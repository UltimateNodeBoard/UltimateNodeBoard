const { execSync } = require('child_process');

console.log('Starting MongoDB container...');

try {
    execSync('docker compose up -d mongodb', { stdio: 'inherit' });
} catch (error) {
    console.error('Failed to start MongoDB container:', error);
    process.exit(1);
}